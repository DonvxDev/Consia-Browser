export function isBlockedUrl(url: string, enabled: boolean): boolean {
  if (!enabled) return false;
  try {
    const u = new URL(url);
    const hostname = u.hostname.replace(/^www\./, "");
    return BLOCKED_DOMAINS.some(
      (domain) => hostname === domain || hostname.endsWith("." + domain)
    );
  } catch {
    return false;
  }
}
